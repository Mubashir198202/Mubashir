package com.usa.javaprectice;

public class Whileloop {

	public static void main(String[] args) {
	
		int i=1;
		
		while(i<=10)
		{
		System.out.println(i);
		i++;	
		}

		int a=2;
		
		while(a<=10)
		{
		System.out.print(a);
		a+=2;
		}
		
		int b=1;
		
		while(b<=10)
		{
		System.out.println(b);
		b+=2;
		}
		
		int c=10;
		
		while(c>=0)
		{
		System.out.print(c);
		c--;
		}
	}

}
